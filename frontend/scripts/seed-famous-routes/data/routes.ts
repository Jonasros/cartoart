/**
 * Get routes by category
 */
export function getRoutesByCategory(category: RouteEntry['category']): RouteEntry[] {
  return allRoutes.filter(route => route.category === category);
}

/**
 * Get routes with direct GPX sources only (priority for initial seeding)
 */
export function getDirectGpxRoutes(): RouteEntry[] {
  return allRoutes.filter(route => route.gpxSource.type === 'direct');
}

/**
 * Route count by category
 */
export function getRouteCounts(): Record<string, number> {
  const counts: Record<string, number> = {};
  for (const route of allRoutes) {
    counts[route.category] = (counts[route.category] || 0) + 1;
  }
  return counts;
}
